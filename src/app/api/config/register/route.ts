export async function PUT(req: Request) {
  try {
    const body = (await req.json()) as any;
    const { id, group_id, subject_id } = body;

    if (!id || !ObjectId.isValid(id)) {
      return NextResponse.json({ error: "ID ‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á" }, { status: 400 });
    }

    const client = await clientPromise;
    const db = client.db("autotable");

    // ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ã‡πâ‡∏≥
    const duplicate = await db.collection("Register").findOne({
      group_id,
      subject_id,
      _id: { $ne: new ObjectId(id) },
    });

    if (duplicate) {
      return NextResponse.json(
        { error: "‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Å‡∏≤‡∏£‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô‡∏ô‡∏µ‡πâ‡∏°‡∏µ‡∏ã‡πâ‡∏≥‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡∏£‡∏∞‡∏ö‡∏ö" },
        { status: 400 },
      );
    }

    const result = await db.collection("Register").updateOne(
      { _id: new ObjectId(id) },
      {
        $set: {
          group_id,
          subject_id,
        },
      },
    );

    if (result.matchedCount === 0) {
      return NextResponse.json(
        { error: "‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç" },
        { status: 404 },
      );
    }

    return NextResponse.json({ message: "‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à" });
  } catch (error) {
    return NextResponse.json(
      { error: "‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç" },
      { status: 500 },
    );
  }
}

